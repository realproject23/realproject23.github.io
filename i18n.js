function setLanguage(lang) {
    localStorage.setItem('yc_language', lang);
    document.documentElement.lang = lang;
    
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerText = translations[lang][key];
        }
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
        const key = el.getAttribute('data-i18n-ph');
        if (translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });

    // Update UI elements like language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if (btn.dataset.lang === lang) {
            btn.classList.add('ui-shadow-in');
            btn.classList.remove('ui-btn-ghost');
        } else {
            btn.classList.remove('ui-shadow-in');
            btn.classList.add('ui-btn-ghost');
        }
    });

    // Custom update for items that might be rendered dynamically
    if (typeof loadRealPrograms === 'function') {
        loadRealPrograms();
    }
    if (typeof updateAdminUI === 'function') {
        updateAdminUI();
    }
}

function getI18nText(key) {
    const lang = localStorage.getItem('yc_language') || 'ko';
    return translations[lang][key] || key;
}

function formatI18n(key, params) {
    let text = getI18nText(key);
    if(params) {
        for (const [k, v] of Object.entries(params)) {
            text = text.replace(`{${k}}`, v);
        }
    }
    return text;
}

